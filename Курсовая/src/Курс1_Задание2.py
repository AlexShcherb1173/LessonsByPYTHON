words_to_decode = ['java', 'python', 'ruby', 'php', 'fortran', 'javascript', 'kotlin', 'swift', 'basic', 'pascal']
def morse_encode(morse, word):
          encoded_word = []
          for char in word:
            if char in morse:
              encoded_word.append(morse[char])
            else:
              encoded_word.append('!')
          return ' '.join(encoded_word)
